package com.java.fundamentals;

        public class Primitives {
        public static void main (String[] args) {

                    // ==================== BYTE ====================


                    byte age = 100;
                    byte subject1Marks = 85;
                    byte subject2Marks = 90;
                    byte subject3Marks = 78;
                    byte subject4Marks = 92;

                    System.out.println("===== BYTE =====");
                    System.out.println("Age: " + age);
                    System.out.println("Subject 1 Marks: " + subject1Marks);
                    System.out.println("Subject 2 Marks: " + subject2Marks);
                    System.out.println("Subject 3 Marks: " + subject3Marks);
                    System.out.println("Subject 4 Marks: " + subject4Marks);

                    // ==================== SHORT ====================


                    short manufacturingYear = 2003;
                    short daysToExpiry = 350;

                    System.out.println("\n===== SHORT =====");
                    System.out.println("Manufacturing Year: " + manufacturingYear);
                    System.out.println("Days to Expiry: " + daysToExpiry);

                    // ==================== INT ====================

                    int rollNo = 53;
                    int otp = 4554;
                    int password = 1849;

                    System.out.println("\n===== INT =====");
                    System.out.println("Age: " + age);
                    System.out.println("Roll No: " + rollNo);
                    System.out.println("OTP: " + otp);
                    System.out.println("Password: " + password);

                    // ==================== LONG ====================

                    long mobileNum = 8985256813L;
                    long population = 16842132216L;
                    long imeiNum = 984465656132L;
                    long adharCardNum = 231637118136L;

                    System.out.println("\n===== LONG =====");
                    System.out.println("Mobile Number: " + mobileNum);
                    System.out.println("Population: " + population);
                    System.out.println("IMEI Number: " + imeiNum);
                    System.out.println("Aadhar Card Number: " + adharCardNum);

                    // ==================== FLOAT ====================
                    float productPrice = 99.99f;
                    float discountPercent = 12.5f;
                    float rating = 4.5f;

                    System.out.println("\n===== FLOAT =====");
                    System.out.println("Product Price: " + productPrice);
                    System.out.println("Discount %: " + discountPercent);
                    System.out.println("Rating: " + rating);

                    // ==================== DOUBLE ====================
                    double salary = 75000.50;
                    double bankBalance = 123456789.99;

                    System.out.println("\n===== DOUBLE =====");
                    System.out.println("Salary: " + salary);
                    System.out.println("Bank Balance: " + bankBalance);

                    // ==================== CHAR ====================
                    char gender = 'M';
                    char grade = 'A';

                    System.out.println("\n===== CHAR =====");
                    System.out.println("Gender: " + gender);
                    System.out.println("Grade: " + grade);

                    // ==================== BOOLEAN ====================
                    boolean isVegetarian = true;
                    boolean isLoggedIn = false;
                    boolean isPassed = true;

                    System.out.println("\n===== BOOLEAN =====");
                    System.out.println("Is Vegetarian: " + isVegetarian);
                    System.out.println("Is Logged In: " + isLoggedIn);
                    System.out.println("Is Passed: " + isPassed);

                }
            }

