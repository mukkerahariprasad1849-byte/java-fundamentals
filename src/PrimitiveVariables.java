public class PrimitiveVariables {
    public static void main (String[] args) {
        // product variables
        int price = 120000;
        long customerMobile = 8985256813L;
        boolean isStock = true;
        float rating = 4.5F;
        System.out.println("== productVariables ===");
        System.out.println("Price:" + price);
        System.out.println("CustomerMobile:" + customerMobile );
        System.out.println("isStock:" + isStock);
        System.out.println("Rating:" + rating);


        //customerVariables
        int id = 2123;
        int age = 22;
        short otp = 2003;
        char gender = 'M';
        long mobileNum = 8985256813L;
        boolean isActive = true;
        double salary = 25000.5;
        float accountBalance = 35000.5F;

        System.out.println("\nCustomerVariables");
        System.out.println("CustomerId:" + id);
        System.out.println("age:" + age);
        System.out.println("otp:" + otp);
        System.out.println("MobileNum:"+ mobileNum);
        System.out.println("Active:" + isActive);
        System.out.println("Salary:"+ salary);
        System.out.println("AccountBalance:"+ accountBalance);


        // OrderVariables
        int orderId = 5673;
        int quantity = 5;
        float discount = 23.3f;
        double totalAmount = 345.56;

        System.out.println("\nOrderVariables");
        System.out.println("OrderId:" + orderId);
        System.out.println("Quaninty:" + quantity);
        System.out.println("TotalAmount:" + totalAmount);




    }
}
